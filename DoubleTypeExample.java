class DoubleTypeExample{
	public static void main(String[] args)
	{
	double ExecutionTime = 1.343;
	double TimeInMiliSeconds = 0.343;
	double PiValue = 3.14;
	double TotalMeters = 5.5;
	double SmallDistance = 2.4;
	System.out.println("ExecutionTime-" +ExecutionTime);
	System.out.println("TimeInMiliSeconds-" +TimeInMiliSeconds);
	System.out.println("PiValue-" +PiValue);
	System.out.println("TotalMeters-" +TotalMeters);
	System.out.println("SmallDistance-" +SmallDistance);
	}
}
