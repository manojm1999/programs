class ShortTypeexample{
	public static void main(String[] args)
	{
	short TotalMarks = 800;
	short NoOfStudents = 500;
	short HousesInVillage = 400;
	short NoOfDaysPerYear = 365;
	short NoOfPagesInBook = 1500;
	System.out.println("TotalMarks-" +TotalMarks);
	System.out.println("NoOfStudents-" +NoOfStudents);
	System.out.println("HousesInVillage-" +HousesInVillage);
	System.out.println("NoOfDaysPerYear-" +NoOfDaysPerYear);
	System.out.println("NoOfPagesInBook-" +NoOfPagesInBook);
	}
}